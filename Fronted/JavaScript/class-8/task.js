// difference b/w push(),pop(),shift(),unshift() with proper defination and example.
// ans> The main difference between the array methods push(), pop(), shift(), and unshift() is we can add or remove array element / items in an array.
// *> Push(): By using this method we can add elements to the end of an Array.
// *>pop(): By using this method we can Remove elements from the end of  an
//          Array.
// *>shift(): Remove an Element from the front of an Array.
// *>unshift(): Add elements to the front of an Array.

// example:
let names= ["pallabi","banita","jayashree","jinu","priya","soni"];

//push():
 console.log(names);
 names.push("sweta");
 console.log(names);
 //pop():
 names.pop();
 console.log(names);
 // shift()
 names.shift();
 console.log(names);
 //unshift():
 names.unshift("rani");
 console.log(names);
 
 
 

 
 










